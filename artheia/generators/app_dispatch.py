#!/usr/bin/env python3
"""
gen_app_dispatch.py

Generates per-application dispatch glue from a PSP root + app signal CSV.
Does NOT generate encode C files (those live in libpsp.so).

Usage:
  python3 tools/gen_app_dispatch.py \\
      --psp-root $PSP_ROOT \\
      --csv config/demo_signals.csv \\
      --out protos/

What it does:
  1. Scan $PSP_ROOT for psp_manifest.json files in any subdirectory
  2. Build a unified signal registry
  3. Read app CSV (signal_name,pdu_name  or  signal_name,message_name)
  4. Validate each CSV entry; warn if not found
  5. Group selected signals by PDU/message → dispatch entries
  6. Copy relevant .pb.h and .pb.c from $PSP_ROOT/proto/ to --out dir
  7. Generate:
     - dispatch_table.h   (cmp_lookup / cmp_can_lookup inlines, externs)
     - dispatch_table.c   (static tables using function pointers from libpsp.so)
     - hercules_filter.h  (constexpr slot list + CAN-ID list)
     - ns_wrapper.h       (C++ typedefs for selected types)
"""

from __future__ import annotations

import csv
import json
import os
import shutil
import sys
from typing import Any, Dict, List, Optional, Tuple

try:
    from jinja2 import Environment, DictLoader
except ImportError:
    sys.exit("ERROR: jinja2 is required.  Run: pip install jinja2")


